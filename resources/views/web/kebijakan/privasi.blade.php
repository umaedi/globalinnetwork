Kebijakan Privasi Portal Informasi Kabupaten Tulang Bawang

1. Pengantar

Portal informasi Kabupaten Tulang Bawang ("Portal") berkomitmen untuk melindungi privasi Anda. Kebijakan Privasi ini menjelaskan bagaimana kami mengumpulkan, menggunakan, dan melindungi data pribadi Anda saat mengakses atau menggunakan layanan kami.

2. Pengumpulan Data Pribadi

Kami dapat mengumpulkan data pribadi Anda melalui:

Informasi yang Anda berikan secara langsung: Nama, email, dan informasi lain yang Anda sediakan saat mengisi formulir atau memberikan umpan balik.

Data yang dikumpulkan secara otomatis: Alamat IP, jenis perangkat, sistem operasi, dan riwayat aktivitas di Portal melalui penggunaan cookie atau teknologi pelacakan lainnya.

3. Penggunaan Data Pribadi

Data yang dikumpulkan akan digunakan untuk:

Memberikan informasi dan layanan yang relevan.

Menanggapi pertanyaan atau permintaan Anda.

Meningkatkan pengalaman pengguna melalui analisis dan evaluasi layanan.

Menyediakan konten yang disesuaikan dengan preferensi Anda.

4. Perlindungan Data

Kami menggunakan langkah-langkah keamanan teknis dan organisasi yang wajar untuk melindungi data pribadi Anda dari akses yang tidak sah, pengungkapan, atau kehilangan. Namun, kami tidak dapat menjamin keamanan data selama transmisi melalui internet.

5. Pengungkapan kepada Pihak Ketiga

Kami tidak menjual atau membagikan data pribadi Anda kepada pihak ketiga tanpa persetujuan Anda, kecuali untuk:

Kepatuhan terhadap kewajiban hukum.

Pemrosesan data oleh penyedia layanan pihak ketiga yang bekerja atas nama kami, dengan kewajiban menjaga kerahasiaan data.

6. Hak Anda

Anda memiliki hak untuk:

Meminta akses, perbaikan, atau penghapusan data pribadi Anda.

Menolak penggunaan data tertentu untuk pemasaran atau analitik.

Menarik persetujuan Anda atas pengumpulan data kapan saja.

7. Cookie dan Teknologi Pelacakan

Kami menggunakan cookie untuk meningkatkan kinerja dan pengalaman Anda di Portal. Anda dapat menonaktifkan cookie melalui pengaturan browser, meskipun hal ini dapat memengaruhi beberapa fungsi Portal.

8. Perubahan Kebijakan Privasi

Kebijakan Privasi ini dapat diperbarui dari waktu ke waktu. Kami menyarankan Anda untuk meninjau halaman ini secara berkala untuk mengetahui perubahan yang mungkin terjadi. Perubahan akan berlaku segera setelah diunggah ke Portal.

